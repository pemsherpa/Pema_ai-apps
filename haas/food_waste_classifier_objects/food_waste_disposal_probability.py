# Define a function to estimate probabilities based on the food item
def estimate_probabilities(food_item):
    food_item_lower = food_item.lower()
    
    # Initial values for probabilities
    landfilled = 0.0
    combusted = 0.0
    composted = 0.0
    anaerobic_dry = 0.0
    anaerobic_wet = 0.0
    
    # Processed or packaged foods are more likely to be landfilled or combusted
    if "sauce" in food_item_lower or "prepacked" in food_item_lower or "canned" in food_item_lower:
        landfilled = 0.6
        combusted = 0.3
        composted = 0.05
        anaerobic_dry = 0.03
        anaerobic_wet = 0.02
    
    # Fresh raw foods (fruits, vegetables) are more likely to be composted or anaerobically digested
    elif "raw" in food_item_lower or "fresh" in food_item_lower or "fruit" in food_item_lower:
        composted = 0.6
        anaerobic_wet = 0.25
        anaerobic_dry = 0.1
        landfilled = 0.03
        combusted = 0.02

    # Dried or processed plant-based foods (e.g., grains, beans) might be composted or digested
    elif "dried" in food_item_lower or "flour" in food_item_lower or "seeds" in food_item_lower:
        composted = 0.5
        anaerobic_dry = 0.3
        anaerobic_wet = 0.15
        landfilled = 0.03
        combusted = 0.02

    # Meats or animal products are more likely to be combusted or landfilled
    elif "meat" in food_item_lower or "fish" in food_item_lower or "egg" in food_item_lower:
        combusted = 0.5
        landfilled = 0.35
        anaerobic_dry = 0.1
        anaerobic_wet = 0.05

    # Alcoholic beverages or liquids might be more combusted or landfilled
    elif "beer" in food_item_lower or "wine" in food_item_lower or "syrup" in food_item_lower:
        combusted = 0.5
        landfilled = 0.4
        anaerobic_wet = 0.05
        anaerobic_dry = 0.05
    
    # Default case for other foods
    else:
        landfilled = 0.4
        combusted = 0.4
        composted = 0.1
        anaerobic_dry = 0.05
        anaerobic_wet = 0.05

    return [landfilled, combusted, composted, anaerobic_dry, anaerobic_wet]

